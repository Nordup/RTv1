#include "libvec.h"

t_vec3	ft_vec_dif(t_vec3 v1, t_vec3 v2)
{
	t_vec3	sum;

	sum.x = v1.x - v2.x;
	sum.y = v1.y - v2.y;
	sum.z = v1.z - v2.z;
	return (sum);
}
